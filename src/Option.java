
public class Option {
	
//	INFO.
//	---------------------------------------------------------------------------
//  This class defines Options a Character/Event/World can choose from.
//	According to the Game creator these Options have different effects, which can be choosen freely
//	---------------------------------------------------------------------------

//	INIT VARIABLES.
//  ---------------------------------------------------------------------------	
	private String option;
//	---------------------------------------------------------------------------
	
	// Constructor, Initializes new Option incl. name
	public Option(String option){
		this.option = option;
	}
	
	// Outputs the Options name
	public String getOption(){
		return option;
	}
	
	
	
	

}
